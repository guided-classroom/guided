f=open('api/lesson.js','w')
f.write('export default async function handler(req, res) {\n')
f.write('res.setHeader("Access-Control-Allow-Origin", "*");\n')
f.write('res.setHeader("Access-Control-Allow-Methods", "POST, OPTIONS");\n')
f.write("res.setHeader('Access-Control-Allow-Headers', 'Content-Type');\n")
f.write('if (req.method === "OPTIONS") { res.status(200).end(); return; }\n')
f.write('try {\n')
f.write('const key = process.env.ANTHROPIC_KEY;\n')
f.write('console.log("key exists:", !!key);\n')
f.write('const response = await fetch("https://api.anthropic.com/v1/messages", {\n')
f.write('method: "POST",\n')
f.write('headers: {\n')
f.write('"content-type": "application/json",\n')
f.write('"x-api-key": key,\n')
f.write('"anthropic-version": "2023-06-01"\n')
f.write('},\n')
f.write('body: JSON.stringify(req.body)\n')
f.write('});\n')
f.write('const data = await response.json();\n')
f.write('res.status(200).json(data);\n')
f.write('} catch(e) {\n')
f.write('res.status(500).json({error: e.message});\n')
f.write('}\n')
f.write('}\n')
f.close()
print('done')
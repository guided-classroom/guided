f=open('progress.html','a')
f.write('async function doGen(){\n')
f.write('if(!cur)return;\n')
f.write('var selected=outcomes.filter(function(o){return o.status!=="none";});\n')
f.write('if(!selected.length){alert("Please rate at least one curriculum expectation first.");return;}\n')
f.write('var sess=cur.sessions||[];\n')
f.write('var rats=sess.length?sess[0].ratings||{}:{};\n')
f.write('var period=document.getElementById("rep-period").value;\n')
f.write('var notes=document.getElementById("extra-notes").value.trim();\n')
f.write('var rStr=QR.map(function(r){var v=rats[r.id]||0;return r.l+": "+v+"%";}).join(", ");\n')
f.write('var trend="";\n')
f.write('if(sess.length>=2){\n')
f.write('var prev=sess[1].ratings||{};\n')
f.write('var imp=QR.filter(function(r){return(rats[r.id]||0)>(prev[r.id]||0);}).map(function(r){return r.l;});\n')
f.write('if(imp.length)trend="Showing improvement in: "+imp.join(", ")+".";\n')
f.write('}\n')
f.write('var strengths=selected.filter(function(o){return o.status==="strength";}).map(function(o){return o.text;});\n')
f.write('var developing=selected.filter(function(o){return o.status==="developing";}).map(function(o){return o.text;});\n')
f.write('var support=selected.filter(function(o){return o.status==="support";}).map(function(o){return o.text;});\n')
f.write('var prompt="You are an experienced teacher writing formal report card comments about a student.\\n";\n')
f.write('prompt+="IMPORTANT: Write all comments FROM THE STUDENT\'S PERSPECTIVE. Describe what the student does, knows, and is working toward. Never say what the teacher will do. Next steps should be about what the student will do next.\\n\\n";\n')
f.write('prompt+="Student: "+cur.name+"\\n";\n')
f.write('prompt+="Grade: "+cur.grade+"\\n";\n')
f.write('prompt+="Reading Level: "+cur.level+"\\n";\n')
f.write('prompt+="Reporting Period: "+period+"\\n";\n')
f.write('prompt+="ReadLead Skill Scores: "+rStr+"\\n";\n')
f.write('if(trend)prompt+="Progress: "+trend+"\\n";\n')
f.write('if(strengths.length)prompt+="Strengths (expectations being met): "+strengths.join("; ")+"\\n";\n')
f.write('if(developing.length)prompt+="Developing (partially meeting): "+developing.join("; ")+"\\n";\n')
f.write('if(support.length)prompt+="Needs Support (not yet meeting): "+support.join("; ")+"\\n";\n')
f.write('if(notes)prompt+="Teacher Notes: "+notes+"\\n";\n')
f.write('prompt+="\\nGenerate 3 different report card comment options. Rules:\\n";\n')
f.write('prompt+="1. Each comment is 3-5 sentences\\n";\n')
f.write('prompt+="2. Student-focused language only (e.g. Marcus demonstrates... Marcus is developing... Marcus will...)\\n";\n')
f.write('prompt+="3. Reference specific expectations and skill scores\\n";\n')
f.write('prompt+="4. Include a clear student-focused next step\\n";\n')
f.write('prompt+="5. Professional, positive, specific tone\\n";\n')
f.write('prompt+="\\nReply ONLY with raw JSON: {\\"comments\\":[{\\"label\\":\\"Strength Focus\\",\\"text\\":\\"...\\"},{\\"label\\":\\"Growth Focus\\",\\"text\\":\\"...\\"},{\\"label\\":\\"Balanced\\",\\"text\\":\\"...\\"}]}";\n')
f.write('document.getElementById("gen-box").innerHTML="<div style=\'text-align:center;padding:16px\'><div style=\'font-size:14px;font-weight:700;color:#A78BFA\'>Generating comments for "+cur.name+"...</div></div>";\n')
f.write('document.getElementById("comments-out").innerHTML="";\n')
f.write('try{\n')
f.write('var res=await fetch("/api/lesson",{method:"POST",headers:{"content-type":"application/json"},body:JSON.stringify({model:"claude-sonnet-4-5",max_tokens:2000,messages:[{role:"user",content:prompt}]})});\n')
f.write('var data=await res.json();\n')
f.write('var text=(data.content||[]).filter(function(b){return b.type==="text";}).map(function(b){return b.text;}).join("").trim();\n')
f.write('var parsed;\n')
f.write('try{parsed=JSON.parse(text);}catch(e){var a=text.indexOf("{"),b=text.lastIndexOf("}");parsed=JSON.parse(text.slice(a,b+1));}\n')
f.write('var comments=parsed.comments||[];\n')
f.write('var html="";\n')
f.write('comments.forEach(function(c,ci){\n')
f.write('html+="<div class=\'comment-box\'>";\n')
f.write('html+="<div style=\'font-size:11px;font-weight:800;color:#A78BFA;margin-bottom:8px\'>"+c.label+"</div>";\n')
f.write('html+="<div style=\'font-size:14px;line-height:1.8;margin-bottom:10px\' id=\'ct-"+ci+"\'>"+c.text+"</div>";\n')
f.write('html+="<button class=\'copy-btn\' onclick=\'copyText("+ci+")\'>Copy to Clipboard</button>";\n')
f.write('html+="</div>";\n')
f.write('});\n')
f.write('document.getElementById("comments-out").innerHTML=html;\n')
f.write('document.getElementById("gen-box").innerHTML="<div style=\'font-size:14px;font-weight:800;color:#A78BFA;margin-bottom:8px\'>Comments Ready!</div><button class=\'btn btn-purple\' style=\'width:100%;font-size:14px\' onclick=\'doGen()\'>Regenerate</button>";\n')
f.write('}catch(e){\n')
f.write('document.getElementById("gen-box").innerHTML="<div style=\'font-size:15px;font-weight:800;margin-bottom:4px\'>Generate Report Comments</div><div style=\'font-size:12px;color:#FF6B6B;margin-bottom:14px\'>Error: "+e.message+"</div><button class=\'btn btn-purple\' style=\'width:100%;font-size:14px\' onclick=\'doGen()\'>Try Again</button>";\n')
f.write('}\n')
f.write('}\n')
f.write('function copyText(i){\n')
f.write('var el=document.getElementById("ct-"+i);\n')
f.write('navigator.clipboard.writeText(el.textContent).then(function(){\n')
f.write('var btns=document.querySelectorAll(".copy-btn");\n')
f.write('btns[i].textContent="Copied!";\n')
f.write('setTimeout(function(){btns[i].textContent="Copy to Clipboard";},2000);\n')
f.write('});\n')
f.write('}\n')
f.write('rdHome();\n')
f.write('</script>\n')
f.write('</body>\n')
f.write('</html>\n')
f.close()
print('progress reporter complete!')